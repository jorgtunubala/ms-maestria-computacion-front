            obj['datosAvalComite'],
            obj['datoSolicitudBeca'],
            obj['requiereFirmaDirector'],
            obj['idDirector'],
            obj['firmaEstudiante'],
            obj['numPaginaTutor'],
            obj['numPaginaDirector'],
            obj['posXTutor'],
            obj['posYTutor'],
            obj['posXDirector'],
            obj['posYDirector'],
            obj['oficioPdf']

    DatosSolicitudCursarAsignatura,
    DatosSolicitudAvalPasantia,
    DatosApoyoPasantia,
    DatosReconoCreditos,
    DatosApoyoCongreso,
    DatosApoyoPublicacion,
    DatosActividadPracticaDocente,
    DatosAvalPracticaDocente,
